#   Reforce o desafio 009, mostrando uma tabuada de um número que o usuario escolher, so que agora utilizando o for.


numero = int(input('Insira um número para realizar a tabuada: '))

for c in range(1, 11):
    print(f'{c:2} x {numero} = {(c * numero)}')
print('fim da tabuada !')



















